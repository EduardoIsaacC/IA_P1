#suma
suma = 10 + 5
print ("suma:", suma)

#resta
resta = 20 - 4
print ("resta:", resta)

#multiplicacion
multi = 6 * 7
print("multi:", multi)

#division normal (devuelve flotante)
divi = 15/2
print("Division:", divi)

#division entera (devuelve solo la parte entera)
divi_entera = 15//2
print ("Division entera:", divi_entera)

#modulo (residuo de la division)
modulo = 15% 2
print ("Residuo:", modulo)

#potencia
potencia = 5 ** 3
print("potencia:", potencia)

#agrupacion con parentesis
operacion = (5+3) * 2
print("resultado:", operacion)

#Lista con edades
edades = [15, 26, 54, 22, 17, 50, 33, 32]

#Obtener la suma de las edades
suma_edades = sum(edades)

#Obtener el n√∫mero de edades
numero_edades = len(edades)

#Calcular la media
media = suma_edades // numero_edades
